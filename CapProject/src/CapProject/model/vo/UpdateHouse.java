package CapProject.model.vo;

public class UpdateHouse {
	private String houseName;
	private String userId;
	private String updateHouseName;
	public String getHouseName() {
		return houseName;
	}
	public void setHouseName(String houseName) {
		this.houseName = houseName;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUpdateHouseName() {
		return updateHouseName;
	}
	public void setUpdateHouseName(String updateHouseName) {
		this.updateHouseName = updateHouseName;
	}
}
